export class IrisPrediction {
    sepal_length:any;
    sepal_width:any;
    petal_length:any;
    petal_width:any;

    constructor(sepal_length:any,sepal_width:any,petal_length:any,petal_width:any){
        this.sepal_length=sepal_length;
        this.sepal_width = sepal_width;
        this.petal_length = petal_length;
        this.petal_width = petal_width;
    }

}